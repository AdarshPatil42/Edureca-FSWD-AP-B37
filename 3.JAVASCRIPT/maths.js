// round the value
console.log(Math.round(95.4)); //ans: 95
console.log(Math.round(41.8)); //ans: 42
// returns nearest integer
console.log(Math.round(34.5)); //ans: 35

// round up side
console.log(Math.ceil(9.2)); //ans:10

// round down side
console.log(Math.floor(20.8)); //ans:20

// get squareroot
console.log(Math.sqrt(256)); //ans:16

console.log(Math.sqrt(10));

// get max value
console.log(Math.max(10,201,301,405,605)); //ans:605
// get min value
console.log(Math.min(10,201,301,405,605));

